      SUBROUTINE DIST
C     PURPOSE... CALCULATES DISTANCE BETWEEN TWO ATOMS
 
      INCLUDE 'dls76.par'
      DIMENSION DELTA1(3)
      INTEGER BT
      character*8 KONT,KARTE,LATOM,CHAR1,WCODE(201),ID,KKAT,IDOM,NCON,
     1            LABEL,CHAR,CHAR2
      character*8 IX,IHD,IHI,IHL,IHR,ISM,IBL1,IDEC,IFAK,IGIN,KOOR,KREF,
     1            ISYMB,LCELL,K3,SYMOPS,ITIT 
      character*8 KTYPE

      COMMON /BLOCK6/ ID(maxat),A(6),AOLD(6),X(3,maxat),XOLD(3,maxat),
     1XINI(3,maxat),FUDGE(3),IFAK(3,maxat),IGIN(6),ITIT(18),XMUL(maxat)
      COMMON /BLOC10/T(maxva),G(3,3),GABL1(3,3,6),GABL2A(3,6,6),
     1GABL2B(3,6,6),GABL2C(3,6,6),KRON(2,2),KRONEK(3,3),DGEL(maxva),MEIG
      COMMON /BLOC16/ ITYPE(maxat),BT(15),IA,NZA,ICON(maxte,11),
     1MD(maxte,18),NCAPID,CAPID(15,4),IAT,IAB,IBT(maxte,4),DOT,
     2WEIGHT(15,3),DELTAX(3),DELTA0(3),DA,DB,ANG,TOT(maxte,4),
     3OTO(maxte,6),PI180,IMOV(3),MSYMAB(maxat),DOBIN(maxdi),
     4KTYPE(20),IDK
      DO 1 K=1,3
      DELTA1(K)=X(K,IAB)-X(K,IAT)
    1 CONTINUE
      DOTQ=0.0
      DO 2 J=1,3
      DO 2 K=1,3
      DOTQ=DOTQ+G(J,K)*DELTA1(J)*DELTA1(K)
    2 CONTINUE
      DOT=SQRT(DOTQ)
      RETURN
      END
      SUBROUTINE ANGLE
C     PURPOSE... CALCULATES ANGLES AT BRIDGING AND CENTRAL ATOMS
 
      INCLUDE 'dls76.par'
      INTEGER BT
      character*8 KTYPE
      COMMON /BLOC10/T(maxva),G(3,3),GABL1(3,3,6),GABL2A(3,6,6),
     1GABL2B(3,6,6),GABL2C(3,6,6),KRON(2,2),KRONEK(3,3),DGEL(maxva),MEIG
      COMMON /BLOC16/ ITYPE(maxat),BT(15),IA,NZA,ICON(maxte,11),
     1MD(maxte,18),NCAPID,CAPID(15,4),IAT,IAB,IBT(maxte,4),DOT,
     2WEIGHT(15,3),DELTAX(3),DELTA0(3),DA,DB,ANG,TOT(maxte,4),
     3OTO(maxte,6),PI180,IMOV(3),MSYMAB(maxat),DOBIN(maxdi),
     4KTYPE(20),IDK
      SCAPR=0.
      DO 1 J=1,3
      DO 1 K=1,3
    1 SCAPR=SCAPR+G(J,K)*DELTAX(J)*DELTA0(K)
      COSA=SCAPR/DA/DB
      IF (ABS(COSA).GE.1.0) COSA=SIGN(1.0,COSA)
      ANG=ACOS(COSA)/PI180
      RETURN
      END
      SUBROUTINE EQUI
C     PURPOSE... TESTS IF DISTANCES NO JS AND NO MS ARE
C     SYMMETRICALLY EQUIVALENT
 
      INCLUDE 'dls76.par'
      DIMENSION NE(4), XA(3,2)
      character*8 KONT,KARTE,LATOM,CHAR1,WCODE(201),ID,KKAT,IDOM,NCON,
     1            LABEL,CHAR,CHAR2
      character*8 IX,IHD,IHI,IHL,IHR,ISM,IBL1,IDEC,IFAK,IGIN,KOOR,KREF,
     1            ISYMB,LCELL,K3,SYMOPS,ITIT 
      character*8 KTYPE

      COMMON /BLOCK2/ N,N1,N2,NV,NV1,NVD,NVG,NVO,NCYC,NCYCLE,NDIAG,NEQU,
     1NFUNC,NRE,M,MATCOR,NRAGA,NCC,NDO,NRH
      COMMON /BLOCK5/ D(maxdi),DOB(maxdi),DOBOLD(maxdi),DELTAD(maxdi),
     1W(maxdi),NN(maxdi,2),LJ(maxdi),RAT(maxrt),ISYMB(maxdi),LDR(maxrt)

      COMMON /BLOCK6/ ID(maxat),A(6),AOLD(6),X(3,maxat),XOLD(3,maxat),
     1XINI(3,maxat),FUDGE(3),IFAK(3,maxat),IGIN(6),ITIT(18),XMUL(maxat)
      COMMON /BLOCK8/ SI(3,maxsy),SIGN(3,3,maxsy),SYMOPS(10,maxat),
     1B(3,3,maxat),BI(3,maxat),IND,MTR(maxat)
      COMMON /BLOC12/ SYMSIG(3,3,100),KATOR(maxdi),NN11(maxdi,2)

      COMMON /BLOC16/ ITYPE(maxat),BT(15),IA,NZA,ICON(maxte,11),
     1MD(maxte,18),NCAPID,CAPID(15,4),IAT,IAB,IBT(maxte,4),DOT,
     2WEIGHT(15,3),DELTAX(3),DELTA0(3),DA,DB,ANG,TOT(maxte,4),
     3OTO(maxte,6),PI180,IMOV(3),MSYMAB(maxat),DOBIN(maxdi),
     4KTYPE(20),IDK
      COMMON /BLOC17/ JS,MS,ITEST,MW(maxdi),MEFF,WRF,IERR,MEQD,MCON,
     1WFAC,IDIMM,IDIMN,IDIMNZ,IDNEQU
C BK     1IDIMM
C     IF NOT THE SAME INDEPENDENT ATOMS
      IF ((NN11(JS,1).NE.NN11(MS,1).OR.NN11(JS,2).NE.NN11(MS,2)).AND.(NN
     111(JS,1).NE.NN11(MS,2).OR.NN11(JS,2).NE.NN11(MS,1))) GO TO 10
      DD=5.E-5
C     COMPARE DISTANCES
      IAT=NN(JS,1)
      IAB=NN(JS,2)
      CALL DIST
      DJS=DOT
      IAT=NN(MS,1)
      IAB=NN(MS,2)
      CALL DIST
      DMS=DOT
      IF (ABS(DMS-DJS).GT.DD) GO TO 10
C     PRODUCE ARTIFICIAL SHIFTS
      LL=2
      IF (NN11(JS,1).EQ.NN11(JS,2)) LL=1
      DO 1 L=1,LL
      DO 1 I=1,3
      XI=0.0
      DO 2 K=1,3
    2 XI=XI+B(I,K,NN11(JS,L))*SIN(FLOAT(K+3*L-3))
    1 XA(I,L)=XI+BI(I,NN11(JS,L))
      IF (LL.EQ.2) GO TO 3
      DO 4 I=1,3
    4 XA(I,2)=XA(I,1)
C     COORDINATES OF DEPENDENT ATOMS
    3 DO 5 LK=1,3,2
      DO 5 L=1,2
      IF (LK.EQ.1) NE(L)=NN(JS,L)-N1
      IF (LK.EQ.3) NE(L+2)=NN(MS,L)-N1
      IF (LK.EQ.3.AND.NN11(JS,1).NE.NN11(MS,1)) NE(L+2)=NN(MS,3-L)-N1
      IF (NE(L+LK-1).LE.0) GO TO 6
      DO 7 I=1,3
      XI=0.0
      DO 8 K=1,3
    8 XI=XI+SIGN(I,K,NE(L+LK-1))*XA(K,L)
    7 X(I,N+L+LK-1)=XI+SI(I,NE(L+LK-1))
      GO TO 5
    6 DO 9 I=1,3
    9 X(I,N+L+LK-1)=XA(I,L)
    5 CONTINUE
C     COMPARE DISTANCES AGAIN
      IAT=N+1
      IAB=N+2
      CALL DIST
      DJS=DOT
      IAT=N+3
      IAB=N+4
      CALL DIST
      DMS=DOT
      IF (ABS(DMS-DJS).GT.DD) GO TO 10
      ITEST=1
      RETURN
   10 ITEST=0
      RETURN
      END
