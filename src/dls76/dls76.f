      SUBROUTINE INVERT
 
      INCLUDE 'dls76.par'
C      MATRIX INVERSION ROUTINE
C      -----------------------
C       BASED ON THE ALGORITHM OF BUSING AND LEVY,
C       COMM.ACM,5,445(1962)
C
C       INVERT IS CALLED BY...MAIN PROGRAM
C       AND CALLS...INCH.
C
C       IF NDIAG=1, A DIAGONAL MATRIX
C       APPROXIMATION IS RUN.
C
      COMMON /BLOCK1/ NTIN,NTOUT,NFILEA,NFILEB,IFLAG
      COMMON /BLOCK2/ N,N1,N2,NV,NV1,NVD,NVG,NVO,NCYC,NCYCLE,NDIAG,NEQU,
     1NFUNC,NRE,M,MATCOR,NRAGA,NCC,NDO,NRH
      COMMON S(maxma)
      COMMON /BLOC10/T(maxva),G(3,3),GABL1(3,3,6),GABL2A(3,6,6),
     1GABL2B(3,6,6),GABL2C(3,6,6),KRON(2,2),KRONEK(3,3),DGEL(maxva),MEIG
      COMMON /BLOC11/ II
      INDEX(J1,J2)=J1*NV+J2-(J1*(J1-1))/2
      IF (NV.GT.1) GO TO 80
      S(1)=1.0/S(1)
      RETURN
   80 CONTINUE
      IF (NDIAG.NE.1) GO TO 2
      DO 99 I=1,NV
      K1=INDEX(I,I)
      S(K1)=1.0/S(K1)
      I1=I+1
      IF (I1.GT.NV) GO TO 99
      DO 98 K=I1,NV
      K1=INDEX(I,K)
      S(K1)=0.0
   98 CONTINUE
   99 CONTINUE
      RETURN
    2 DO 102 II=1,NV
      K1=INDEX(II-1,II)
      S(K1)=FLOAT(II)
      BIGS=0.0
      DO 100 J=II,NV
      K2=INDEX(J,J)
      PABL=ABS(S(K2))
      IF (PABL.LE.BIGS) GO TO 100
      BIGS=PABL
      S(K1)=FLOAT(J)
  100 CONTINUE
      CALL INCH
      K3=INDEX(II,II)
      PABL=ABS(S(K3))
      IF (PABL.GT.0.0) GO TO 1
      WRITE (NTOUT,1000) II
 1000 FORMAT (1HO//5H *** , 25HWARNING: DIAGONAL ELEMENT,I4,  9H VANISHE
     1S,4H ***//1H , 20HJOB KILLED BY INVERT)
      STOP
