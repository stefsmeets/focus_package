      DO 102 K=J,NV
      K1=INDEX(J,K)
      K2=INDEX(II-1,K)
      K3=INDEX(II,J)
      S(K1)=S(K1)+S(K2)*S(K3)
  102 CONTINUE
   10 CONTINUE
      I1=NV-1
      LL=NV
      DO 106 L=1,I1
      II=LL-L
      I=II+1
      IF (I.GT.NV) GO TO 11
      DO 103 J=I,NV
      K1=INDEX(II,J)
  103 S(K1)=0.0
      DO 105 J=I,NV
      DO 104 K=J,NV
      K1=INDEX(II,K)
      K2=INDEX(II-1,J)
      K3=INDEX(J,K)
      S(K1)=S(K1)+S(K2)*S(K3)
      IF (J.EQ.K) GO TO 104
      K1=INDEX(II,J)
      K2=INDEX(II-1,K)
      S(K1)=S(K1)+S(K2)*S(K3)
  104 CONTINUE
      K1=INDEX(II,II)
      K2=INDEX(II-1,J)
      K3=INDEX(II,J)
      S(K1)=S(K1)+S(K2)*S(K3)
  105 CONTINUE
   11 CONTINUE
  106 CALL INCH
      RETURN
      END
